#include "push_swap.h"

void	print(t_list *lst)
{
	if (lst == NULL)
	{
		printf("[]\n");
		return ;
	}
	printf("[");
	for (t_list *i = lst; i != NULL; i = i->next)
		printf("%d%s", i->value, i->next == NULL ? "]\n" : ", ");
}

int	main(int ac, char **av)
{
	t_list	*a;
	t_list	*b;

	a = NULL;
	b = NULL;
	if (ac < 2)
		return (0);
	if (!get_element(&a, ac, av))
	{
		put_str("Error\n", 2);
		return (1);
	}
	printf("a : ");
	print(a);
	printf("b : ");
	print(b);
	lst_clear(&a);
	lst_clear(&b);
	return (0);
}
