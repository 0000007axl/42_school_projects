#include "push_swap.h"

void	reverse_rotate(t_list **stack)
{
	t_list	*tmp;

	tmp = lst_pop_back(stack);
	lst_push_front(stack, tmp);
}

void	rra(t_list **a)
{
	reverse_rotate(a);
	write(1, "rra\n", 4);
}

void	rrb(t_list **b)
{
	reverse_rotate(b);
	write(1, "rrb\n", 4);
}
void	rrr(t_list **a, t_list **b)
{
	reverse_rotate(a);
	reverse_rotate(b);
	write(1, "rrr\n", 4);
}